			<div id="tab-1" class="tab-content">
				<p>Nach der Vereinbarung eines Termins beginnen wir zunächst mit einer umfangreichen Bestandsaufnahme, bei der wir uns ein ausführliches Bild der Situation vor Ort machen. Es wird geprüft, welche Mittel bereits vorhanden sind und welche noch nötig sind, um unsere Produkte einwandfrei nutzen zu können.</p>
			</div>
			
			<div id="tab-2" class="tab-content">
				<p>Als nächstes kümmern wir uns um die Planung ihres Wunschprojekts. Hierbei wird nach individueller Beratung mit dem Kunden ein Entwurf erstellt, nach welchem später gearbeitet wird. Anschließend folgt die Kalkulation der Kosten, wobei sämtliche Aspekte wie z.B. die räumliche Beschaffenheit vor Ort mit einbezogen werden.</p>  
			</div>
			
			<div id="tab-3" class="tab-content">
				<p>Sobald alle Planungen in Absprache mit dem Kunden abgeschlossen wurden, geht es an die Vorbereitung. Hierbei präperieren wir den späteren Ofenstandort so, dass ihr gewünschtes Objekt ideal platizert werden kann. Falls nötig, werden beispielsweiße Umstellungen am Kaminanschluss vorgenommen oder ein Funkenschutz angebracht, damit ihre Sicherheit später optimal gewährleistet werden kann. Natürlich gilt auch bei uns: Sauberkeit ist oberstes Gebot. Das bedeuted, wir kümmern uns selbstverständlich um den Schutz ihrer Einrichtung vor Staub und anderem Schmutz.</p>
			</div>
			
			<div id="tab-4" class="tab-content">
				<p>Wurden alle nötigen Vorbereitungen vorgenommen, kann die Montage des jeweiligen Ofenobjektes beginnen. Durch das vorherige Präparieren der Unterlage kann hierbei zunächst mit der Platzierung eines optimalen Unterbaus für den späteren Ofen begonnen werden. Anschließend folgt der Aufbau des Hauptteils und die schlussendliche Prüfung aller Bestandteile durch unser Fachpersonal.</p>
			</div>
		
			<div id="tab-5" class="tab-content">
				<p>Nun sind wir auch schon fertig: Ihr gewünschter Ofen ist montiert, überprüft und bereit für seine erste Nutzung. Selbstverständlich werden wir uns nach der Montage nocheinmal mit Ihnen in Verbindung setzen, um einen Termin für die erste Inbetriebnahme zu vereinbahren. Hierbei besuchen wir Sie nocheinmal, um Sie über alles zu unterrichten, was bei der Nutzung des jeweiligen Produkts zu beachten ist. Natürlich kümmern wir uns auch um die rechtliche Betriebserlaubnis und anderen relveanten Dinge wie das sog. <a target="_blank" href="http://de.wikipedia.org/wiki/Typenschild">Typenschild</a>. Gerne machen wir Ihnen auch ein Angebot bezüglich der Lieferung weiterer Brennstoffe.</p>
